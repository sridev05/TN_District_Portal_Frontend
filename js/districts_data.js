// 5. Enhanced search and filter functionality
function initializeSearchAndFilter() {
  const searchInput = document.getElementById('district-search');
  const filterSelect = document.getElementById('district-filter');

  if (searchInput) {
    searchInput.addEventListener('input', function() {
      filterDistricts(this.value, filterSelect ? filterSelect.value : 'all');
    });
  }

  if (filterSelect) {
    filterSelect.addEventListener('change', function() {
      filterDistricts(searchInput ? searchInput.value : '', this.value);
    });
  }
}

// 6. Enhanced filter districts function
function filterDistricts(searchTerm, category) {
  const districtCards = document.querySelectorAll('.district-card');
  
  districtCards.forEach(card => {
    const districtName = card.querySelector('.district-name-en').textContent.toLowerCase();
    const districtContent = card.textContent.toLowerCase();
    
    const matchesSearch = districtName.includes(searchTerm.toLowerCase()) || 
                         districtContent.includes(searchTerm.toLowerCase());
    
    let matchesCategory = true;
    if (category !== 'all') {
      // Enhanced category filtering
      const districtData = districts.find(d => d.name_en.toLowerCase() === districtName);
      if (districtData) {
        switch(category) {
          case 'industrial':
            matchesCategory = districtData.economy.toLowerCase().includes('industry') || 
                            districtData.economy.toLowerCase().includes('manufacturing');
            break;
          case 'tourism':
            matchesCategory = districtData.economy.toLowerCase().includes('tourism') || 
                            districtData.attractions.length > 100;
            break;
          case 'agricultural':
            matchesCategory = districtData.economy.toLowerCase().includes('agriculture') || 
                            districtData.economy.toLowerCase().includes('farming');
            break;
          case 'coastal':
            matchesCategory = districtData.economy.toLowerCase().includes('fishing') || 
                            districtData.economy.toLowerCase().includes('port');
            break;
        }
      }
    }
    
    if (matchesSearch && matchesCategory) {
      card.style.display = 'block';
    } else {
      card.style.display = 'none';
    }
  });
}

