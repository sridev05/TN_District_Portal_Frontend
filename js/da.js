// 3. Enhanced toggle functions
function toggleMoreInfo(districtName) {
  const moreInfoDiv = document.getElementById(`more-info-${districtName}`);
  const button = event.target.closest('.more-info-btn');
  
  if (moreInfoDiv.style.display === 'none' || moreInfoDiv.style.display === '') {
    moreInfoDiv.style.display = 'block';
    button.innerHTML = '<i class="icon-info"></i> Less Information';
  } else {
    moreInfoDiv.style.display = 'none';
    button.innerHTML = '<i class="icon-info"></i> More Information';
  }
}

function toggleSchemes(districtName) {
  const schemesDiv = document.getElementById(`schemes-${districtName}`);
  const button = event.target.closest('.schemes-btn');
  
  if (schemesDiv.style.display === 'none' || schemesDiv.style.display === '') {
    schemesDiv.style.display = 'block';
    button.innerHTML = '<i class="icon-list"></i> Hide Schemes';
  } else {
    schemesDiv.style.display = 'none';
    button.innerHTML = '<i class="icon-list"></i> Government Schemes';
  }
}

